package edu.wpi.cs.wpisuitetcw.modules.planningpoker.controllers;

import java.util.TimerTask;

import edu.wpi.cs.wpisuitetcw.modules.planningpoker.models.PlanningPokerSession;
import edu.wpi.cs.wpisuitetcw.modules.planningpoker.view.ViewEventManager;
import edu.wpi.cs.wpisuitetcw.modules.planningpoker.view.overviews.SessionTableModel;

public class EndSessionOnDeadlineController extends TimerTask {

	private PlanningPokerSession session;
	
	public EndSessionOnDeadlineController(PlanningPokerSession session){
		this.session = session;
	}
	
	/* (non-Javadoc)
	 * @see java.util.TimerTask#run()
	 */
	@Override
	public void run() {
		session.close();
		session.save();
		SessionTableModel.getInstance().update();

	}

}
